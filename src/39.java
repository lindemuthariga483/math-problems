public class MathProblems {
    public static void main(String[] args) {
        // Example of simple calculations
        System.out.println("1 + 2 = " + (1 + 2));  // Output: 3

        // Example of arithmetic operations
        double result = 5.0 / 2;
        System.out.println("5 / 2 = " + result);  // Output: 2.5

        // Example of mathematical functions
        int age = 18;
        if (age >= 18) {
            System.out.println("You are old enough to vote.");
        } else {
            System.out.println("You are not old enough to vote yet.");
        }

        // Example of loops and conditional statements
        for (int i = 0; i < 5; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}
